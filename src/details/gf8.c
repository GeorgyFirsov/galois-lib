unsigned char gf8_multiply(unsigned char lhs, unsigned char rhs)
{
    unsigned int temporary;

    if (lhs == 0 || rhs == 0)
    {
        return 0;
    }

    //
    // The algorithm is very simple, it uses the following 
    // obvious observation:
    //
    //          a * b = exp(log(a) + log(b))
    //

    temporary = gf8p_log_lookup_table[lhs] + gf8p_log_lookup_table[rhs];
    return gf8p_exp_lookup_table[temporary % 255];
}
